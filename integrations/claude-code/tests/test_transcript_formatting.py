class TestTranscriptFormatting:
    """Tests for transcript formatting issues."""

    def test_double_newlines_between_messages(self, tmp_path: Path) -> None:
        """Messages should be separated by double newlines within each step."""
        transcript_path = tmp_path / "transcript.jsonl"
        document_id = "transcript"

        transcript_path.write_text(
            "\n".join(
                [
                    json.dumps(
                        {
                            "uuid": "msg1",
                            "parentUuid": None,
                            "type": "user",
                            "timestamp": "2024-01-21T14:30:00Z",
                            "message": {"content": "First message"},
                        }
                    ),
                    json.dumps(
                        {
                            "uuid": "msg2",
                            "parentUuid": "msg1",
                            "type": "assistant",
                            "timestamp": "2024-01-21T14:30:05Z",
                            "message": {
                                "content": [{"type": "text", "text": "Response"}]
                            },
                        }
                    ),
                    json.dumps(
                        {
                            "uuid": "msg3",
                            "parentUuid": "msg2",
                            "type": "user",
                            "timestamp": "2024-01-21T14:30:10Z",
                            "message": {"content": "Second message"},
                        }
                    ),
                ]
            )
            + "\n"
        )

        client = FakeTranscriptClient()
        execute_sync(transcript_path, document_id, client)

        # With step-level chunking: 3 messages = 3 steps
        assert len(client.appends) == 3

        # Each step contains one message
        assert "First message" in client.appends[0][1]
        assert "Response" in client.appends[1][1]
        assert "Second message" in client.appends[2][1]

    def test_slash_commands_simplified(self, tmp_path: Path) -> None:
        """Slash commands should be simplified from XML format."""
        transcript_path = tmp_path / "transcript.jsonl"
        document_id = "transcript"

        # The XML command format that Claude Code produces
        command_content = (
            "Caveat: The messages below were generated by the user while running "
            "local commands. DO NOT respond to these messages or otherwise consider "
            "them in your response unless the user explicitly asks you to."
            "<command-name>/clear</command-name>"
            "<command-message>clear</command-message>"
            "<command-args></command-args>"
        )

        transcript_path.write_text(
            json.dumps(
                {
                    "uuid": "msg1",
                    "parentUuid": None,
                    "type": "user",
                    "timestamp": "2024-01-21T14:30:00Z",
                    "message": {"content": command_content},
                }
            )
            + "\n"
        )

        client = FakeTranscriptClient()
        execute_sync(transcript_path, document_id, client)

        assert len(client.appends) == 1
        text = client.appends[0][1]

        # Should NOT contain the verbose XML
        assert "<command-name>" not in text
        assert "<command-message>" not in text
        assert "<command-args>" not in text
        assert "Caveat:" not in text

        # Should contain the simplified command
        assert "/clear" in text

    def test_local_stdout_removed(self, tmp_path: Path) -> None:
        """Empty local-command-stdout tags should be removed."""
        transcript_path = tmp_path / "transcript.jsonl"
        document_id = "transcript"

        transcript_path.write_text(
            json.dumps(
                {
                    "uuid": "msg1",
                    "parentUuid": None,
                    "type": "user",
                    "timestamp": "2024-01-21T14:30:00Z",
                    "message": {
                        "content": "<local-command-stdout></local-command-stdout>"
                    },
                }
            )
            + "\n"
        )

        client = FakeTranscriptClient()
        execute_sync(transcript_path, document_id, client)

        # Empty messages should result in no append
